// ###### calc.js ######
//
// calc.js is a simple calculator.
//
// current available operations:
//
//  - addition: add(x, y)
//  - subtraction: sub(x, y)
//  - multiplication: mult(x, y)
//  - division: div(x,y)

// adds two numbers
exports.add = function(x, y) {
  return x + y;
}

// subtracts two numbers
exports.sub = function(x, y) {
  return x - y;
}

// multiplies two numbers
exports.mult = function(x, y) {
  return x * y;
}